# Calculate Convex Hull Volume
get_hull <- function(sp, trait_space) {
  pts <- trait_space[rownames(trait_space) %in% sp, , drop = FALSE] %>% 
    na.omit() %>% 
    unique()
  
  d <- ncol(pts)
  # Points must be > dimensions + 1 for a valid volume
  if (nrow(pts) < d + 1) return(NULL)
  
  hull <- tryCatch(
    convhulln(as.matrix(pts), options = "FA"),
    error = function(e) NULL
  )
  
  if (is.null(hull)) return(NULL)
  
  list(hull = hull, vol = hull$vol, pts = as.matrix(pts))
}

# Calculate Intersection Volume using Monte Carlo Integration
calc_overlap <- function(h1, h2, n_points = 10000) {
  all_pts <- rbind(h1$pts, h2$pts)
  d <- ncol(all_pts)
  
  b_min <- apply(all_pts, 2, min)
  b_max <- apply(all_pts, 2, max)
  b_vol <- prod(b_max - b_min)
  
  if(b_vol <= 0) return(0)
  
  rand_pts <- matrix(runif(n_points * d), n_points, d) %>% 
    sweep(2, b_max - b_min, "*") %>% 
    sweep(2, b_min, "+")
  
  in1 <- tryCatch(inhulln(h1$hull, rand_pts), error = function(e) rep(FALSE, n_points))
  in2 <- tryCatch(inhulln(h2$hull, rand_pts), error = function(e) rep(FALSE, n_points))
  
  inter_vol <- b_vol * mean(in1 & in2, na.rm = TRUE)
  return(min(inter_vol, h1$vol, h2$vol))
}

