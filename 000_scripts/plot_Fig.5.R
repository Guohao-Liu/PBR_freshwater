# Identify organism type based on filename code
get_organism <- function(f) {
  code <- substr(tools::file_path_sans_ext(basename(f)), 4, 5)
  case_when(
    code == "BD" ~ "Diatoms",
    code == "BM" ~ "Macroinvertebrates",
    code == "FI" ~ "Fish",
    code == "PP" ~ "Phytoplankton",
    code == "ZP" ~ "Zooplankton",
    code == "AP" ~ "Aquatic plants",
    TRUE ~ "Others"
  )
}

