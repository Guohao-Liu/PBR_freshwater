# 3. Core Processing Function
# ==============================================================================

process_dataset_by_dim <- function(f) {
  d_name <- tools::file_path_sans_ext(basename(f))
  org    <- get_organism(f)
  message(">>> Processing: ", d_name, " (", org, ")")
  
  # Read Data
  abu <- read_excel(f, "species")
  tra <- read_excel(f, "traits")
  env <- read_excel(f, "environment")
  
  colnames(abu)[1] <- "Site"
  colnames(env)[1] <- "Site"
  colnames(tra)[1] <- "Species"
  
  # Site selection and TP grouping (Top/Mid/Bottom 20% or 25%)
  richness <- tibble(Site = as.character(abu$Site), S = rowSums(abu[, -1] > 0, na.rm = TRUE))
  env_clean <- env %>% 
    mutate(Site = as.character(Site), TP = as.numeric(TP)) %>% 
    filter(Site %in% richness$Site[richness$S >= 3], !is.na(TP))
  
  if (nrow(env_clean) < 15) return(NULL)
  
  n_e <- floor(nrow(env_clean) * 0.20)
  if (n_e < 2) return(NULL)
  
  env_ordered <- env_clean %>% arrange(TP)
  n_total     <- nrow(env_ordered)
  
  low_sites  <- env_ordered$Site[1:n_e]
  high_sites <- env_ordered$Site[(n_total - n_e + 1):n_total]
  mid_start  <- floor((n_total - n_e) / 2) + 1
  mid_sites  <- env_ordered$Site[mid_start:(mid_start + n_e - 1)]
  
  tp_gps <- bind_rows(
    tibble(Site = low_sites,  gp = "low"),
    tibble(Site = mid_sites,  gp = "mid"),
    tibble(Site = high_sites, gp = "high")
  )
  
  # Presence-Absence Community Matrix
  comm <- abu %>% filter(Site %in% tp_gps$Site) %>% column_to_rownames("Site") %>% decostand("pa")
  org_map <- trait_map %>% filter(Organism == org)
  
  # Loop through Dimensions
  map_dfr(names(broad_dims), function(dim_name) {
    target_traits <- org_map %>% filter(Trait_category %in% broad_dims[[dim_name]]) %>% pull(Original_trait)
    
    # Exact column matching
    actual_cols <- colnames(tra)[unlist(map(target_traits, ~ which(str_detect(colnames(tra), paste0("^", .x, "$")))))]
    actual_cols <- unique(actual_cols)
    
    if (length(actual_cols) < 1) return(NULL)
    
    # Robust Cleaning: Handling data types and missing values
    tra_sub <- tra %>% 
      select(Species, all_of(actual_cols)) %>% 
      filter(!is.na(Species)) %>%
      mutate(across(all_of(actual_cols), ~ as.numeric(as.character(.)))) %>% 
      na.omit() %>%
      distinct(Species, .keep_all = TRUE) %>% 
      column_to_rownames("Species")
    
    # Remove columns with zero variance
    tra_sub <- tra_sub[, apply(tra_sub, 2, function(x) sd(x, na.rm=TRUE)) > 0, drop=FALSE]
    
    if (nrow(tra_sub) < 5 || ncol(tra_sub) < 1) return(NULL)
    
    # Construct PCoA Space
    dist_m <- daisy(tra_sub, metric = "gower")
    pcoa_res <- tryCatch(pcoa(dist_m, correction = "cailliez"), error = function(e) NULL)
    if (is.null(pcoa_res)) return(NULL)
    
    n_ax <- min(n_axes_max, sum(pcoa_res$values$Eigenvalues > 0.0001), nrow(tra_sub) - 1)
    if (n_ax < 2) return(NULL)
    
    trait_space <- as.data.frame(pcoa_res$vectors[, 1:n_ax, drop = FALSE])
    
    # Bootstrap iteration
    map_dfr(1:n_iter, function(i) {
      get_sp <- function(target_gp) {
        target_sites <- tp_gps$Site[tp_gps$gp == target_gp]
        s_sites <- sample(target_sites, length(target_sites), replace = TRUE)
        present <- colSums(comm[rownames(comm) %in% s_sites, colnames(comm) %in% rownames(tra_sub), drop=FALSE]) > 0
        names(present)[present]
      }
      
      s_l <- get_sp("low"); s_m <- get_sp("mid"); s_h <- get_sp("high")
      pairs <- list(lm = list(s_l, s_m), mh = list(s_m, s_h), lh = list(s_l, s_h))
      
      map_dfr(names(pairs), function(p_name) {
        h1 <- get_hull(pairs[[p_name]][[1]], trait_space)
        h2 <- get_hull(pairs[[p_name]][[2]], trait_space)
        if (is.null(h1) || is.null(h2)) return(NULL)
        
        v_i <- calc_overlap(h1, h2, n_mc)
        tibble(Comparison = p_name, Loss = (h1$vol - v_i)/h1$vol, Gain = (h2$vol - v_i)/h2$vol)
      })
    }) %>% mutate(Dimension = dim_name)
  }) %>% mutate(Dataset = d_name, Organism = org)
}

